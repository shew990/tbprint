    public DBOutList(string strErpVoucherNo)
    {
        InitializeComponent();
        T_OutStockInfo model = Common.getoutstock(strErpVoucherNo);
        if (model != null)
        {
            Header header = new Header();
            header.Dc1 = model.ErpVoucherNo; 
            header.Dc2 = model.VouUser;
            header.Dc3 = model.ERPNote;
            header.Dc4 = Convert.ToDateTime(model.VouDate).ToString("yyyy/MM//dd HH:mm:ss");
            header.Dc5 = model.StrVoucherType;
            header.Dc6 = model.strReviewUserNo;

            objectDataSource1.DataSource = header;
            if (model.lstDetail != null && model.lstDetail.Count > 0)
            {
                for (int i = 0; i < model.lstDetail.Count; i++)
                {
                    DataRow row = detailDS1.DataTable1.NewRow();
                    row["Dc1"] = model.lstDetail[i].RowNo;
                    row["Dc2"] = model.lstDetail[i].MaterialDesc;
                    row["Dc3"] = model.lstDetail[i].OutStockQty;
                    row["Dc4"] = model.lstDetail[i].FromErpWarehouse;
                    row["Dc5"] = model.lstDetail[i].EDate;
                    row["Dc6"] = model.lstDetail[i].ToErpWarehouse;
                    
                    detailDS1.Tables[0].Rows.Add(row);
                }
            }

        }

    }

