    public HMSaleOutOutList(string strErpVoucherNo)
    {
        InitializeComponent();
        T_OutStockInfo model = Common.getoutstock(strErpVoucherNo);
        if (model != null)
        {
            Header header = new Header();
            header.Dc1 = Convert.ToDateTime(model.VouDate).ToString("yyyy/MM/dd");
            header.Dc2 = model.CustomerName;
            header.Dc3 = model.Address1;
            header.Dc4 = Convert.ToDateTime(model.VouDate).ToString("HH:mm:ss");
            header.Dc5 = model.erp;
            header.Dc6 = model.VouUser;
            header.Dc7 = model.Phone;
            header.Dc8 = model.ERPNote;
            header.Dc9 = model.strReviewUserNo;

            objectDataSource1.DataSource = header;
            if (model.lstDetail != null && model.lstDetail.Count > 0)
            {
                for (int i = 0; i < model.lstDetail.Count; i++)
                {
                    DataRow row = detailDS1.DataTable1.NewRow();
                    row["Dc1"] = i + 1;
                    row["Dc2"] = model.lstDetail[i].MaterialDesc;
                    row["Dc3"] = model.lstDetail[i].OutStockQty;
                    row["Dc4"] = model.lstDetail[i].Price;
                    row["Dc5"] = model.lstDetail[i].Amount;
                    row["Dc6"] = model.lstDetail[i].EAN;
                    detailDS1.Tables[0].Rows.Add(row);
                }
            }

        }


    }

